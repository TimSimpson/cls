def main():
    if len(sys.argv) > 1:
        choice = int(sys.argv[1])
    elif len(sys.argv) > 2:
        print('Usage: cls [0-{}]'.format(len(sys.argv[0])))
        return 1
    else:
        r = random.random()
        max = len(art)
        choice = int(r * max)

    if choice > len(art):
        print('Invalid selection "{}". Pick a number from 0 - {}.'
              .format(choice, len(art)))
        return 1
    print(art[choice])
    return 0


